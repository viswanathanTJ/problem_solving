#PYTHON RELATIONAL OPERATORS
print(10 > 15)
print(10 >= 15)
print(10 < 15)
print(10 <= 15)
print(10 == 15)
print(10 != 15)

#PYTHON MATHEMATICAL OPERATORS
print(10 - 15)
print(10 * 15)
print(10 / 15)
print(10 // 15)
print(10 % 15)
print(10 ** 15)

#PYTHON BITWISE OPERATORS
print(10 & 15)
print(10 | 15)
print(10 ^ 15)
print(~15)
print(10 >> 15)
print(10 << 15)